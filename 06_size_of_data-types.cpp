#include<iostream>

using namespace std;

int main ()
{
    int integerType;
    char charType;
    float floatType;
    double doubleType;

    cout << "size of int is: " << sizeof(integerType) << "bytes\n";
    cout << "size of char is: " << sizeof(charType) << "bytes\n";
    cout << "size of float is: " << sizeof(floatType) << "bytes\n";
    cout << "size of double is: " << sizeof(doubleType) << "bytes\n";


return 0;
}